F_num = int(input("Give me the first number: "))
S_num = int(input("Give me the second number: "))
print("Thank you!")
add = F_num + S_num
sub = F_num - S_num
div = F_num / S_num
mul = F_num * S_num

print(str(F_num) + " + " + str(S_num) + " = " + str(add))
print(str(F_num) + " - " + str(S_num) + " = " + str(sub))
print(str(F_num) + " / " + str(S_num) + " = " + str(div))
print(str(F_num) + " * " + str(S_num) + " = " + str(mul))
